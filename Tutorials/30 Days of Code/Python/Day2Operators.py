def solve(meal_cost, tip_percent, tax_percent):
    tip_amount = tip_percent / 100 * meal_cost
    tax_amount = tax_percent / 100 * meal_cost
    total_amount = meal_cost + tip_amount + tax_amount
    
    print(round(total_amount))

